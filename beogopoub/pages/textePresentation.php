<p>La ville de Ouagadougou est estimée aujourd’hui à environ pus de 2.000.000 d’habitants.
 Face à cette explosion démographique, la capitale burkinabè est confrontée à des problèmes
  d’hygiène et d’assainissement dus à une production importante de déchets. En effet 300.000 
  tonnes de déchets sont collectées par an dans la ville de Ouagadougou. Soit environ 800 tonnes 
  par jour. Chaque habitant de Ouagadougou produit environ 0,5 kg de déchets par jour. Cela a pour
   conséquence la pollution du cadre de vie et donc un danger pour la santé des populations.<p>

<p>Aujourd’hui, plus que jamais, la question écologique, le réchauffement climatique, la pollution, 
    le recyclage, la gestion des déchets sont au cœur de l’actualité de notre société. Au Burkina Faso, 
    plusieurs structures d’assainissement ont vu le jour dans l’optique de résoudre ces problèmes
     écologiques. Cependant, la collecte des ordures par ces structures se fait suivant des itinéraires
      et des horaires fixes. Les poubelles sont vidées chaque jour ou chaque semaine qu’elles soient
       remplies ou pas. Ceci entraine des couts superflus, une mauvaise optimisation de l’équipement 
       et les poubelles pleines durant plusieurs jours provoquent des nuisances. 
    C’est en ce sens qu’ai venu l’idée de mettre en place une poubelle écologique intelligente dénommée "BEOGOPOUB".</p>
    
